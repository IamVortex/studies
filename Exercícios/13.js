// Crie um programa que exibe se um dia é dia útil, fim de semana ou dia inválido dado o número referente ao
// dia. Considere que domingo é o dia 1 e sábado é o dia 7. Utilize a estrutura Switch.

const util = '2, 3, 4, 5, 6'
const fimDeSemana = '1, 7'

switch (util) {
    case util:
        console.log(`Os dias ${util} são dias úteis`)
        break
}

switch (fimDeSemana) {
    case fimDeSemana:
        console.log(`Os dias ${fimDeSemana} não são dias úteis`)
        break
}